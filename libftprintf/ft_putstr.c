#include "../so_long.h"

int	ft_putstr(char *str)
{
	int	i;

	i = 0;
	if (str == 0)
	{
		write (1, "(null)", 6);
		return (6);
	}
	else
	{
		while (str[i])
			write (1, &str[i++], 1);
	}
	return (i);
}
